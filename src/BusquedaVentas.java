    private void jTConsultasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTConsultasMouseClicked
        if(nombreProducto != null){
            codigo.setText(jTConsultas.getValueAt(jTConsultas.getSelectedRow(), 0).toString());
            nombreProducto.setText(jTConsultas.getValueAt(jTConsultas.getSelectedRow(), 1).toString());
            //consulto en la base de datos para mostrar demas datos del producto
            try{
                String sql = "SELECT * FROM productos WHERE id = '"+jTConsultas.getValueAt(jTConsultas.getSelectedRow(), 0).toString()+"' ";
                ResultSet rs = conexion.sentencia.executeQuery(sql);
                if(rs.next()){
                    bodega.setText(rs.getString("cantidad"));
                    stock.setText(rs.getString("stock"));
                    precio.setText("$ "+rs.getString("precio_venta_unidad"));
                    cantidadVenta.setText("1");
                    //CARGO LA IMAGEN
                    Blob foto = rs.getBlob("imagen");
                    if(foto!=null){
                        byte[] byteHuella = foto.getBytes(1,(int)foto.length());
                        fotografia2 = new File("temp/" + rs.getString("id") + ".jpg");
                        FileOutputStream canalSalida = new FileOutputStream(fotografia2);
                        canalSalida.write(byteHuella);
                        canalSalida.close();
                        //MOSTRAR LA IMAGEN EN LA VISTA PREVIA
                        ImageIcon imagenFoto = new ImageIcon("temp/"+rs.getString("id") + ".jpg");
                        Image imageAuxiliar = imagenFoto.getImage();
                        imageAuxiliar = imageAuxiliar.getScaledInstance(imagen.getWidth(), imagen.getHeight(), Image.SCALE_AREA_AVERAGING);
                        imagenFoto.setImage(imageAuxiliar);
                        imagen.setIcon(imagenFoto);
                        imagen.updateUI();
                    }
                    
                }
            }catch(Exception er){
                JOptionPane.showMessageDialog(null, "error mostrando demas datos : "+er.toString());
            }
        }
        setVisible(false);
    }//GEN-LAST:event_jTConsultasMouseClicked


